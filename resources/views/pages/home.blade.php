                                    <h2>Was ist Herz-Kohärenz?</h2>
                                    <p>Eine rhythmische Atemtechnik, die dein Herz, dein Gehirn und dein Nervensystem
                                        synchronisiert. Durch Atmen mit 6 Zyklen pro Minute (5s Einatmen, 5s Ausatmen)
                                        verwandelst du deinen chaotischen Herzschlag in eine regelmäßige und harmonische
                                        Welle. Diese Synchronisation schafft ein tiefes Gleichgewicht: dein Stress nimmt
                                        ab, dein Geist klärt sich, dein Körper regeneriert sich. Einfach, schnell,
                                        wissenschaftlich erwiesen.
                                    </p>

                                </div>
