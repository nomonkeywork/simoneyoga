                                    <h3>Puis-je pratiquer allongé ou dois-je être assis ?</h3>
                                </div>
                                <div class="reponse">
                                    <p>Il est préférable de pratiquer assis ou debout plutôt qu'allongé. La
                                        position horizontale modifie la façon dont ton système cardiovasculaire réagit à
                                        la
                                        respiration rythmée, ce qui réduit l'efficacité de la cohérence cardiaque.
                                        Assis, tu
                                        peux
                                        être sur une chaise, dans un fauteuil, ou même dans ta voiture. L'important est
                                        d'avoir
                                        le
                                        dos relativement droit sans être rigide, et d'être confortablement installé. Tu
                                        peux
                                        t'adosser si tu veux, pas besoin d'une posture parfaite comme en méditation.
                                        Debout
                                        fonctionne aussi très bien si tu es en déplacement ou que tu veux rester
                                        discret.</p>
