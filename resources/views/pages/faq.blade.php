                                <div class="question">
                                    <h3>Y a-t-il des contre-indications ?</h3>
                                </div>
                                <div class="reponse">
                                    <p>La cohérence cardiaque est remarquablement sûre et accessible à presque
                                        tout le monde. Aucun effet secondaire indésirable n'a été rapporté dans les
                                        études.
                                        Quelques
                                        précautions toutefois : si tu portes un pacemaker (stimulateur cardiaque), tu
                                        peux
                                        pratiquer
                                        sans problème, mais l'appareil régule artificiellement ton rythme donc les
                                        effets seront
                                        différents. Si tu souffres de maladies rénales sévères, demande l'avis de ton
                                        médecin
                                        avant
                                        de commencer. Si tu débutes et que tu as du mal à respirer lentement, ne force
                                        pas :
                                        commence par des respirations plus superficielles et progresse graduellement. Si
                                        tu
                                        ressens
                                        des étourdissements ou des palpitations, c'est que tu respires trop fort,
                                        respire plus
                                        doucement. Dans tous les cas, la cohérence cardiaque ne remplace jamais un
                                        traitement
                                        médical, c'est un complément qui vient en soutien.</p>
                                </div>
