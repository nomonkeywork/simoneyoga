                                    <h3>Pourquoi exactement 5 minutes par exercice ?</h3>
                                </div>
                                <div class="reponse">
                                    <p>La durée de 5 minutes n'est pas arbitraire. Les recherches montrent que dès
                                        2-3 minutes de pratique, ton corps commence à réagir : ta pression artérielle
                                        baisse et
                                        ton
                                        cortisol diminue. Mais c'est à partir de 5 minutes que tu atteins un état de
                                        cohérence
                                        stable et profond. Les effets d'une session de 5 minutes durent ensuite environ
                                        4 à 5
                                        heures
                                        dans ton corps. C'est pourquoi la méthode recommande 3 sessions quotidiennes :
                                        pour
                                        maintenir ces bénéfices tout au long de ta journée. Moins de 3 minutes ne
                                        produit pas
                                        d'effets significatifs. Si tu veux pratiquer plus de 5 minutes parce que ça te
                                        fait du
                                        bien,
                                        tu peux bien sûr le faire, mais pour l'efficacité optimale, mieux vaut répéter
                                        plusieurs
                                        sessions courtes dans la journée.</p>
