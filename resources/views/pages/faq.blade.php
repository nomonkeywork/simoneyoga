                                <div class="question">
                                    <h3>Was ist Herz-Kohärenz?</h3>
                                </div>
                                <div class="reponse">
                                    <p>La cohérence cardiaque est une technique de respiration rythmée qui
                                        synchronise ton cœur, ton cerveau et ton système nerveux. Imagine ton rythme
                                        cardiaque
                                        comme
                                        une musique : au repos, il est naturellement irrégulier, un peu chaotique. En
                                        respirant
                                        à un
                                        rythme précis de 6 respirations par minute (5 secondes d'inspiration, 5 secondes
                                        d'expiration), tu transformes ce chaos en une onde régulière et harmonieuse,
                                        comme une
                                        vague
                                        douce et stable. Cette synchronisation crée un état d'équilibre dans ton corps
                                        qui
                                        active
                                        ton système nerveux parasympathique, celui qui te calme et te régénère. Pas
                                        besoin de
                                        vider
                                        ton esprit comme en méditation, il suffit juste de suivre le rythme de ta
                                        respiration.
                                    </p>
                                </div>
